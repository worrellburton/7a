// Type-only import — erased at build so jsPDF (~300 KB) stays out of
// the bundle that imports this module. The constructor is loaded
// lazily inside openEligibilityPdf via dynamic import.
import type { jsPDF } from 'jspdf';

// One-page, Seven Arrows-branded insurance eligibility PDF.
//
// Renders the 271 response stored on a VOB row into a single letter
// page using pure jsPDF primitives plus an embedded logo PNG. All
// text stays selectable; the file ends up ~300KB (logo dominates).
//
// Layout, top to bottom:
//   1. Branded header band (logo inverted onto dark sienna)
//   2. Patient name hero + status verdict pill
//   3. Patient / Insurance two-column data card
//   4. Benefits matrix tiles (deductible, OOP, copay, coinsurance, dates)
//   5. Covered-services chip row
//   6. Notes / warnings strip (Stedi aaaErrors, etc.)
//   7. Branded footer band (contact info, trace id, generated by)
//
// The shape of `eligibility_response` follows Stedi's JSON 271 model.
// Keys we lean on:
//   planStatus[]              { statusCode, planCoverageDescription, serviceTypeCodes, planDateInformation? }
//   benefitsInformation[]     { code, name, serviceTypeCodes, inPlanNetworkIndicator, benefitAmount, benefitPercent, benefitDate }
//   subscriber                { firstName, lastName, dateOfBirth, memberId }
//   payer                     { name, payorIdentification }
//   errors[] / payer.aaaErrors

