// ─── ASCII safety (Helvetica WinAnsi can't render fancy glyphs) ───

function asciiSafe(input: string | null | undefined): string {
  if (!input) return '';
  return String(input)
    .replace(/≥/g, '>=')
    .replace(/≤/g, '<=')
    .replace(/→/g, '->')
    .replace(/←/g, '<-')
    .replace(/—|–/g, '-')
    .replace(/•/g, '-')
    .replace(/[‘’]/g, "'")
    .replace(/[“”]/g, '"')
    .replace(/…/g, '...')
    .replace(/©/g, '(c)');
}

// ─── Eligibility response parser ──────────────────────────────────

type Verdict = 'active' | 'inactive' | 'review';

interface Highlights {
  verdict: Verdict;
  verdictLabel: string;
  planName: string | null;
  effectiveDate: string | null;
  terminationDate: string | null;
  // tiles: [label, primary value, secondary value (optional)]
  tiles: Array<{ label: string; value: string; sub?: string }>;
  coveredServices: string[];
  warnings: string[];
}

const SERVICE_NICENAMES: Record<string, string> = {
  '1': 'Medical care',
  '30': 'Health benefit plan',
  '33': 'Chiropractic',
  '35': 'Dental care',
  '47': 'Hospital',
  '48': 'Hospital inpatient',
  '50': 'Hospital outpatient',
  '51': 'Hospital ER',
  '86': 'Emergency services',
  '88': 'Pharmacy',
  '98': 'Office visit',
  'A4': 'Psychiatric',
  'AD': 'Inpatient psychiatric',
  'AE': 'Psychiatric, outpatient',
  'AF': 'Psychiatric, day partial',
  'AG': 'Psychiatric, residential',
  'AI': 'Substance abuse',
  'AJ': 'Substance abuse, inpatient',
  'AK': 'Substance abuse, outpatient',
  'BH': 'Mental health',
  'MH': 'Mental health',
  'BG': 'Mental health, partial',
  'BF': 'Mental health, residential',
  'UC': 'Urgent care',
};

function parseHighlights(resp: Record<string, unknown> | null): Highlights {
  const empty: Highlights = {
    verdict: 'review',
    verdictLabel: 'Needs review',
    planName: null,
    effectiveDate: null,
    terminationDate: null,
    tiles: [],
    coveredServices: [],
    warnings: [],
  };
  if (!resp) return empty;

  const warnings: string[] = [];
  const aaaErrors = ((resp.payer as Record<string, unknown> | undefined)?.aaaErrors as Array<Record<string, unknown>>) || [];
  for (const e of aaaErrors) {
    const desc = String(e.description || '');
    const code = String(e.code || '');
    if (desc) warnings.push(`AAA${code ? ' ' + code : ''}: ${desc}`);
  }
  const errors = (resp.errors as Array<Record<string, unknown>>) || [];
  for (const e of errors) {
    const desc = String(e.description || '');
    if (desc && !warnings.includes(desc)) warnings.push(desc);
  }

  const planStatus = (resp.planStatus as Array<Record<string, unknown>>) || [];
  let verdict: Verdict = warnings.length > 0 && planStatus.length === 0 ? 'review' : 'review';
  let verdictLabel = 'Needs review';
  let planName: string | null = null;
  for (const p of planStatus) {
    const sc = String(p.statusCode || '').toUpperCase();
    const name = (p.planCoverageDescription as string) || null;
    if (!planName && name) planName = name;
    if (sc === '1' || sc === 'A') {
      verdict = 'active';
      verdictLabel = 'Active coverage';
      break;
    }
    if (sc === '6' || sc === 'I' || sc === '7') {
      verdict = 'inactive';
      verdictLabel = 'Inactive';
    }
  }
  if (planStatus.length === 0 && warnings.length === 0) {
    verdict = 'review';
    verdictLabel = 'Needs review';
  }

  // Pull benefit summary fields.
  const benefits = (resp.benefitsInformation as Array<Record<string, unknown>>) || [];

  let effectiveDate: string | null = null;
  let terminationDate: string | null = null;
  const tiles: Array<{ label: string; value: string; sub?: string }> = [];

  const pickAmount = (code: string, networkPref?: 'Y' | 'N'): { in?: string; out?: string } => {
    let inAmt: string | undefined;
    let outAmt: string | undefined;
    for (const b of benefits) {
      if (String(b.code || '') !== code) continue;
      const amt = (b.benefitAmount as string) || (b.benefitPercent as string);
      if (!amt) continue;
      const net = String(b.inPlanNetworkIndicator || '').toUpperCase();
      const formatted = formatBenefitValue(amt, b.benefitPercent ? '%' : '$');
      if (net === 'Y') inAmt = formatted;
      else if (net === 'N') outAmt = formatted;
      else if (!inAmt) inAmt = formatted;
    }
    if (networkPref === 'Y') return { in: inAmt };
    if (networkPref === 'N') return { out: outAmt };
    return { in: inAmt, out: outAmt };
  };

  const deductible = pickAmount('C');
  const oopMax = pickAmount('G');
  const copay = pickAmount('B');
  const coinsurance = pickAmount('A');

  // Effective / termination dates from any benefit dateRange.
  for (const b of benefits) {
    const dt = b.benefitDate as Record<string, unknown> | string | undefined;
    if (dt && typeof dt === 'object') {
      const start = dt.startDate as string | undefined;
      const end = dt.endDate as string | undefined;
      if (start && !effectiveDate) effectiveDate = start;
      if (end && !terminationDate) terminationDate = end;
    } else if (typeof dt === 'string' && !effectiveDate) {
      effectiveDate = dt;
    }
  }

  // Build tiles in display order.
  tiles.push(makeTile('Plan status', verdictLabel, planName || undefined));
  tiles.push(makeTile('Deductible', deductible.in || '—', deductible.out ? `Out-of-net ${deductible.out}` : undefined));
  tiles.push(makeTile('Out-of-pocket max', oopMax.in || '—', oopMax.out ? `Out-of-net ${oopMax.out}` : undefined));
  tiles.push(makeTile('Co-pay', copay.in || '—', copay.out ? `Out-of-net ${copay.out}` : undefined));
  tiles.push(makeTile('Coinsurance', coinsurance.in || '—', coinsurance.out ? `Out-of-net ${coinsurance.out}` : undefined));
  tiles.push(
    makeTile(
      'Plan dates',
      effectiveDate ? formatPlanDate(effectiveDate) : '—',
      terminationDate ? `Ends ${formatPlanDate(terminationDate)}` : undefined,
    ),
  );

  // Covered service-type codes — gather distinct codes that appear
  // in either planStatus or benefitsInformation.
  const codeSet = new Set<string>();
  for (const p of planStatus) {
    const arr = (p.serviceTypeCodes as string[] | undefined) || [];
    for (const c of arr) codeSet.add(c);
  }
  for (const b of benefits) {
    const arr = (b.serviceTypeCodes as string[] | undefined) || [];
    for (const c of arr) codeSet.add(c);
  }
  const coveredServices: string[] = [];
  for (const code of codeSet) {
    const nice = SERVICE_NICENAMES[code];
    if (nice) coveredServices.push(nice);
  }

  return {
    verdict,
    verdictLabel,
    planName,
    effectiveDate,
    terminationDate,
    tiles,
    coveredServices: dedupe(coveredServices).slice(0, 12),
    warnings,
  };
}

function makeTile(label: string, value: string, sub?: string) {
  return { label, value, sub };
}

function formatBenefitValue(raw: string, kind: '$' | '%'): string {
  const n = Number(raw);
  if (!Number.isFinite(n)) return raw;
  if (kind === '%') return `${Math.round(n)}%`;
  return `$${n.toLocaleString('en-US', { minimumFractionDigits: 0, maximumFractionDigits: 2 })}`;
}

function formatPlanDate(raw: string): string {
  // Stedi returns YYYYMMDD or YYYY-MM-DD
  const m = /^(\d{4})-?(\d{2})-?(\d{2})/.exec(raw);
  if (!m) return raw;
  const y = Number(m[1]);
  const mo = Number(m[2]);
  const d = Number(m[3]);
  const date = new Date(Date.UTC(y, mo - 1, d));
  return date.toLocaleDateString('en-US', { month: 'short', day: 'numeric', year: 'numeric', timeZone: 'UTC' });
}

function dedupe<T>(arr: T[]): T[] {
  return Array.from(new Set(arr));
}

// ─── Logo loading ────────────────────────────────────────────────

let logoCache: string | null = null;
async function loadLogoDataUrl(): Promise<string | null> {
  if (logoCache) return logoCache;
  if (typeof window === 'undefined') return null;
  try {
    const res = await fetch('/images/logo.png');
    if (!res.ok) return null;
    const blob = await res.blob();
    const reader = new FileReader();
    const dataUrl: string = await new Promise((resolve, reject) => {
      reader.onload = () => resolve(String(reader.result));
      reader.onerror = () => reject(reader.error);
      reader.readAsDataURL(blob);
    });
    logoCache = dataUrl;
    return dataUrl;
  } catch {
    return null;
  }
}

// ─── Primitive helpers ───────────────────────────────────────────

function setFill(pdf: jsPDF, hex: string) {
  const { r, g, b } = hexToRgb(hex);
  pdf.setFillColor(r, g, b);
}
function setStroke(pdf: jsPDF, hex: string) {
  const { r, g, b } = hexToRgb(hex);
  pdf.setDrawColor(r, g, b);
}
function setText(pdf: jsPDF, hex: string) {
  const { r, g, b } = hexToRgb(hex);
  pdf.setTextColor(r, g, b);
}
function hexToRgb(hex: string): { r: number; g: number; b: number } {
  const h = hex.replace('#', '');
  return {
    r: parseInt(h.slice(0, 2), 16),
    g: parseInt(h.slice(2, 4), 16),
    b: parseInt(h.slice(4, 6), 16),
  };
}

function drawPill(
  pdf: jsPDF,
  x: number,
  y: number,
  text: string,
  opts: { fill: string; textColor: string; padX?: number; size?: number; bold?: boolean },
): number {
  const { fill, textColor, padX = 10, size = 10, bold = true } = opts;
  pdf.setFont('helvetica', bold ? 'bold' : 'normal');
  pdf.setFontSize(size);
  const w = pdf.getTextWidth(text) + padX * 2;
  const h = size + 8;
  setFill(pdf, fill);
  pdf.roundedRect(x, y - h + 4, w, h, h / 2, h / 2, 'F');
  setText(pdf, textColor);
  pdf.text(text, x + padX, y);
  return w;
}

