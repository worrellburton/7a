export interface EligibilityPdfRow {
  id: string;
  full_name: string;
  date_of_birth: string | null;
  phone: string | null;
  email: string | null;
  insurance_provider: string | null;
  member_id?: string | null;
  group_number?: string | null;
  payer_id?: string | null;
  payer_name?: string | null;
  subscriber_first_name?: string | null;
  subscriber_last_name?: string | null;
  subscriber_dob?: string | null;
  subscriber_relationship?: string | null;
  eligibility_response?: Record<string, unknown> | null;
  eligibility_checked_at?: string | null;
}

// When `existingWindow` is supplied and still open we navigate it to
// the new blob URL instead of spawning a fresh popup — this is what
// lets "Re-run eligibility check" refresh an already-open PDF
// viewer without leaving stale data on screen.
export async function openEligibilityPdf(
  row: EligibilityPdfRow,
  existingWindow?: Window | null,
): Promise<Window | null> {
  const resp = row.eligibility_response ?? null;
  const highlights = parseHighlights(resp);
  const logoDataUrl = await loadLogoDataUrl();
  const { jsPDF: JsPdfCtor } = await import('jspdf');
  const pdf = new JsPdfCtor({ unit: 'pt', format: 'letter' });

  drawHeaderBand(pdf, logoDataUrl, row);
  drawHero(pdf, row, highlights);
  drawDataCard(pdf, row);
  drawBenefitsMatrix(pdf, highlights);
  drawCoveredServices(pdf, highlights);
  drawWarnings(pdf, highlights);
  drawFooterBand(pdf, row);

  const blobUrl = pdf.output('bloburl') as unknown as string;

  if (existingWindow && !existingWindow.closed) {
    try {
      existingWindow.location.replace(blobUrl);
      existingWindow.focus();
      return existingWindow;
    } catch {
      // Cross-origin / blob navigation failures fall through to a
      // fresh window so the user still sees the updated PDF.
    }
  }

  const w = window.open(blobUrl, '_blank', 'noopener,noreferrer,width=900,height=1100');
  if (!w) {
    const safe = row.full_name.replace(/[^a-z0-9]+/gi, '-').toLowerCase() || 'patient';
    pdf.save(`eligibility-${safe}.pdf`);
    return null;
  }
  return w;
}

// ─── Section: header band ───────────────────────────────────────

function drawHeaderBand(pdf: jsPDF, logoDataUrl: string | null, row: EligibilityPdfRow) {
  const H = 78;
  setFill(pdf, COLOR.deep);
  pdf.rect(0, 0, PAGE_W, H, 'F');

  // Accent stripe along the bottom edge of the band.
  setFill(pdf, COLOR.primary);
  pdf.rect(0, H, PAGE_W, 3, 'F');

  // Logo (inverted: original is dark-on-light, we render the
  // dreamcatcher + wordmark over a dark band so we just place the
  // image — its multi-color gradient reads on the deep sienna).
  if (logoDataUrl) {
    try {
      const aspect = 3176 / 865;
      const logoH = 44;
      const logoW = logoH * aspect;
      pdf.addImage(logoDataUrl, 'PNG', MARGIN_X, (H - logoH) / 2, logoW, logoH, undefined, 'FAST');
    } catch {
      drawLogoFallback(pdf);
    }
  } else {
    drawLogoFallback(pdf);
  }

  // Right side: doc title + verification id
  setText(pdf, COLOR.white);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(10);
  pdf.text(asciiSafe('INSURANCE ELIGIBILITY VERIFICATION'), PAGE_W - MARGIN_X, 32, { align: 'right' });

  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(8);
  setText(pdf, '#d8c8b8');
  const checkedAt = row.eligibility_checked_at
    ? new Date(row.eligibility_checked_at).toLocaleString('en-US', {
        month: 'short', day: 'numeric', year: 'numeric', hour: 'numeric', minute: '2-digit',
      })
    : new Date().toLocaleString('en-US');
  pdf.text(asciiSafe(`Verified ${checkedAt}`), PAGE_W - MARGIN_X, 48, { align: 'right' });

  const traceId = ((row.eligibility_response?.meta as Record<string, unknown> | undefined)?.traceId as string | undefined) || row.id;
  pdf.setFontSize(7);
  setText(pdf, '#a89484');
  pdf.text(asciiSafe(`Verification ID: ${traceId.slice(0, 36)}`), PAGE_W - MARGIN_X, 62, { align: 'right' });
}

function drawLogoFallback(pdf: jsPDF) {
  setText(pdf, COLOR.white);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(18);
  pdf.text(asciiSafe('SEVEN ARROWS'), MARGIN_X, 38);
  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(8);
  setText(pdf, '#d8c8b8');
  pdf.text(asciiSafe('RECOVERY'), MARGIN_X, 54);
}

// ─── Section: patient hero + status pill ─────────────────────────

function drawHero(pdf: jsPDF, row: EligibilityPdfRow, h: Highlights) {
  const TOP = 100;

  // Eyebrow label
  setText(pdf, COLOR.primary);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(8);
  pdf.text(asciiSafe('PATIENT'), MARGIN_X, TOP);

  // Big name in a serif (Times — selectable & built into jsPDF)
  setText(pdf, COLOR.ink);
  pdf.setFont('times', 'normal');
  pdf.setFontSize(32);
  pdf.text(asciiSafe(row.full_name || 'Unknown patient'), MARGIN_X, TOP + 26);

  // Sub-line: DOB + payer in muted body
  setText(pdf, COLOR.muted);
  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(10);
  const subParts: string[] = [];
  if (row.date_of_birth) subParts.push(`DOB ${formatPlanDate(row.date_of_birth)}`);
  const payer = row.payer_name || row.insurance_provider;
  if (payer) subParts.push(payer);
  if (h.planName) subParts.push(h.planName);
  pdf.text(asciiSafe(subParts.join('   '))  || '', MARGIN_X, TOP + 44);

  // Verdict pill on the right edge.
  const { fill, fg } = verdictColors(h.verdict);
  drawPill(pdf, PAGE_W - MARGIN_X - 1, TOP + 20, asciiSafe(h.verdictLabel.toUpperCase()), {
    fill,
    textColor: fg,
    size: 11,
    padX: 14,
    bold: true,
  });
  // The drawPill returns width starting from x, but here we want right-anchored.
  // Re-draw aligned right: clear and re-add manually.
  redrawPillRight(pdf, PAGE_W - MARGIN_X, TOP + 20, asciiSafe(h.verdictLabel.toUpperCase()), fill, fg);
}

function redrawPillRight(pdf: jsPDF, xRight: number, y: number, text: string, fill: string, fg: string) {
  // Overwrite the left-anchored pill with a properly right-anchored one.
  // We paint a small background-color rectangle behind first to mask
  // the earlier draw.
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(11);
  const w = pdf.getTextWidth(text) + 28;
  const h = 22;
  // Mask: paint page background (white) over the area where the
  // earlier left-anchored pill might be.
  setFill(pdf, COLOR.white);
  pdf.rect(MARGIN_X + 200, y - h + 4, PAGE_W - MARGIN_X - (MARGIN_X + 200), h, 'F');
  // Actual pill
  setFill(pdf, fill);
  pdf.roundedRect(xRight - w, y - h + 4, w, h, h / 2, h / 2, 'F');
  setText(pdf, fg);
  pdf.text(text, xRight - 14, y, { align: 'right' });
}

function verdictColors(v: Verdict): { fill: string; fg: string } {
  if (v === 'active') return { fill: COLOR.emerald, fg: COLOR.white };
  if (v === 'inactive') return { fill: COLOR.red, fg: COLOR.white };
  return { fill: COLOR.amber, fg: COLOR.white };
}

// ─── Section: data card (patient + insurance) ────────────────────

function drawDataCard(pdf: jsPDF, row: EligibilityPdfRow) {
  const TOP = 178;
  const H = 120;
  const COL_GAP = 18;
  const colW = (PAGE_W - MARGIN_X * 2 - COL_GAP) / 2;

  // Card background
  setFill(pdf, COLOR.cardBg);
  pdf.roundedRect(MARGIN_X, TOP, PAGE_W - MARGIN_X * 2, H, 8, 8, 'F');
  setStroke(pdf, COLOR.hairline);
  pdf.setLineWidth(0.6);
  pdf.roundedRect(MARGIN_X, TOP, PAGE_W - MARGIN_X * 2, H, 8, 8, 'S');

  // Vertical divider between columns
  setStroke(pdf, COLOR.hairline);
  pdf.line(MARGIN_X + colW + COL_GAP / 2, TOP + 16, MARGIN_X + colW + COL_GAP / 2, TOP + H - 16);

  drawCardColumn(pdf, MARGIN_X + 18, TOP + 18, colW - 18, 'PATIENT', [
    ['Name', row.full_name],
    ['Date of birth', row.date_of_birth ? formatPlanDate(row.date_of_birth) : '—'],
    ['Phone', row.phone || '—'],
    ['Email', row.email || '—'],
  ]);
  drawCardColumn(pdf, MARGIN_X + colW + COL_GAP + 18, TOP + 18, colW - 18, 'INSURANCE', [
    ['Payer', row.payer_name || row.insurance_provider || '—'],
    ['Member ID', row.member_id || '—'],
    ['Group #', row.group_number || '—'],
    [
      'Subscriber',
      [row.subscriber_first_name, row.subscriber_last_name].filter(Boolean).join(' ') || row.full_name,
    ],
    ['Relationship', row.subscriber_relationship || 'Self'],
  ]);
}

function drawCardColumn(
  pdf: jsPDF,
  x: number,
  y: number,
  width: number,
  heading: string,
  rows: Array<[string, string | null | undefined]>,
) {
  setText(pdf, COLOR.primary);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(8);
  pdf.text(asciiSafe(heading), x, y);

  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(9);
  let cy = y + 16;
  for (const [label, raw] of rows) {
    const value = (raw || '—') as string;
    setText(pdf, COLOR.muted);
    pdf.text(asciiSafe(label), x, cy);
    setText(pdf, COLOR.ink);
    pdf.setFont('helvetica', 'bold');
    const wrapped = pdf.splitTextToSize(asciiSafe(value), width - 110) as string[];
    pdf.text(wrapped[0] || '—', x + 100, cy);
    pdf.setFont('helvetica', 'normal');
    cy += 17;
  }
}

// ─── Section: benefits matrix (2 rows x 3 columns) ───────────────

function drawBenefitsMatrix(pdf: jsPDF, h: Highlights) {
  const TOP = 318;
  const ROWS = 2;
  const COLS = 3;
  const GAP = 10;
  const usableW = PAGE_W - MARGIN_X * 2;
  const tileW = (usableW - GAP * (COLS - 1)) / COLS;
  const tileH = 80;

  // Section label
  setText(pdf, COLOR.primary);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(8);
  pdf.text(asciiSafe('BENEFITS SUMMARY'), MARGIN_X, TOP - 8);

  setStroke(pdf, COLOR.hairline);
  pdf.setLineWidth(0.5);
  pdf.line(MARGIN_X + 110, TOP - 11, PAGE_W - MARGIN_X, TOP - 11);

  for (let i = 0; i < ROWS * COLS; i++) {
    const tile = h.tiles[i];
    if (!tile) continue;
    const col = i % COLS;
    const r = Math.floor(i / COLS);
    const x = MARGIN_X + col * (tileW + GAP);
    const y = TOP + r * (tileH + GAP);
    drawBenefitTile(pdf, x, y, tileW, tileH, tile.label, tile.value, tile.sub);
  }
}

function drawBenefitTile(
  pdf: jsPDF,
  x: number,
  y: number,
  w: number,
  h: number,
  label: string,
  value: string,
  sub?: string,
) {
  setFill(pdf, COLOR.cardBgAlt);
  pdf.roundedRect(x, y, w, h, 6, 6, 'F');
  setStroke(pdf, COLOR.hairline);
  pdf.setLineWidth(0.6);
  pdf.roundedRect(x, y, w, h, 6, 6, 'S');

  // Left accent stripe — narrow vertical bar in primary.
  setFill(pdf, COLOR.primary);
  pdf.rect(x, y + 8, 3, h - 16, 'F');

  // Label
  setText(pdf, COLOR.muted);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(7);
  pdf.text(asciiSafe(label.toUpperCase()), x + 14, y + 18);

  // Value (large)
  setText(pdf, COLOR.ink);
  pdf.setFont('times', 'normal');
  pdf.setFontSize(20);
  const valueLines = pdf.splitTextToSize(asciiSafe(value), w - 24) as string[];
  pdf.text(valueLines[0] || '', x + 14, y + 46);

  // Sub
  if (sub) {
    setText(pdf, COLOR.muted);
    pdf.setFont('helvetica', 'normal');
    pdf.setFontSize(8);
    const subLines = pdf.splitTextToSize(asciiSafe(sub), w - 24) as string[];
    pdf.text(subLines[0] || '', x + 14, y + h - 12);
  }
}

// ─── Section: covered services chips ─────────────────────────────

function drawCoveredServices(pdf: jsPDF, h: Highlights) {
  const TOP = 510;
  setText(pdf, COLOR.primary);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(8);
  pdf.text(asciiSafe('COVERED SERVICES'), MARGIN_X, TOP);

  setStroke(pdf, COLOR.hairline);
  pdf.setLineWidth(0.5);
  pdf.line(MARGIN_X + 108, TOP - 3, PAGE_W - MARGIN_X, TOP - 3);

  if (h.coveredServices.length === 0) {
    setText(pdf, COLOR.muted);
    pdf.setFont('helvetica', 'italic');
    pdf.setFontSize(9);
    pdf.text(asciiSafe('No service-type detail returned by payer.'), MARGIN_X, TOP + 18);
    return;
  }

  // Wrap chips across up to 3 lines.
  let cx = MARGIN_X;
  let cy = TOP + 18;
  const lineH = 22;
  const maxRight = PAGE_W - MARGIN_X;
  for (const svc of h.coveredServices) {
    pdf.setFont('helvetica', 'normal');
    pdf.setFontSize(9);
    const text = asciiSafe(svc);
    const w = pdf.getTextWidth(text) + 18;
    if (cx + w > maxRight) {
      cx = MARGIN_X;
      cy += lineH;
      if (cy > TOP + 18 + lineH * 2) break;
    }
    setFill(pdf, COLOR.emeraldBg);
    pdf.roundedRect(cx, cy - 11, w, 16, 8, 8, 'F');
    setText(pdf, COLOR.emerald);
    pdf.text(text, cx + 9, cy);
    cx += w + 6;
  }
}

// ─── Section: warnings / aaaErrors ───────────────────────────────

function drawWarnings(pdf: jsPDF, h: Highlights) {
  if (h.warnings.length === 0) return;
  const TOP = 600;
  const HMAX = 70;

  setFill(pdf, COLOR.amberBg);
  pdf.roundedRect(MARGIN_X, TOP, PAGE_W - MARGIN_X * 2, HMAX, 6, 6, 'F');
  setStroke(pdf, '#e7c177');
  pdf.setLineWidth(0.6);
  pdf.roundedRect(MARGIN_X, TOP, PAGE_W - MARGIN_X * 2, HMAX, 6, 6, 'S');

  // Tag stripe
  setFill(pdf, COLOR.amber);
  pdf.rect(MARGIN_X, TOP, 3, HMAX, 'F');

  setText(pdf, COLOR.amber);
  pdf.setFont('helvetica', 'bold');
  pdf.setFontSize(8);
  pdf.text(asciiSafe('REVIEW REQUIRED'), MARGIN_X + 14, TOP + 16);

  setText(pdf, COLOR.body);
  pdf.setFont('helvetica', 'normal');
  pdf.setFontSize(9);
  let wy = TOP + 30;
  for (const w of h.warnings.slice(0, 3)) {
    const wrapped = pdf.splitTextToSize(asciiSafe(`- ${w}`), PAGE_W - MARGIN_X * 2 - 24) as string[];
    for (const line of wrapped.slice(0, 1)) {
      pdf.text(line, MARGIN_X + 14, wy);
      wy += 12;
      if (wy > TOP + HMAX - 6) break;
    }
    if (wy > TOP + HMAX - 6) break;
  }
}

